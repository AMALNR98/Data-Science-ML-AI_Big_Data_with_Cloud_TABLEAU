# For loop
"""
Synatx :
    for i in range(start,end):
        statement

syntax with incrment :
    for i in range(start,end,incrment):
        statement

for loop works up to end-1
"""

for i in range (1,10):
    print(i)

for j in range(1,10,2):
    print(j)

for k in range(10): # 0 - 9 default is 1
    print(k)