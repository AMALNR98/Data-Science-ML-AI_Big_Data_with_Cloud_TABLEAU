def addition(number_1, number_2):
    result = number_1 + number_2
    return result


def subtraction(number_1, number_2):
    result = number_1 - number_2
    return result


def division(number_1, number_2):
    result = number_1 / number_2
    return result


def multiplication(number_1, number_2):
    result = number_1 * number_2
    return result
